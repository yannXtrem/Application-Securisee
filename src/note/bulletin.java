    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        if(listeNotes.getSelectedRowCount() == 1) {
            int l = listeNotes.getSelectedRow();

            Note n = new Note(
                etudiant.getMatricule(),
                listeNotes.getModel().getValueAt(l, 0).toString(),
                Integer.parseInt(listeNotes.getModel().getValueAt(l, 1).toString())
            );
            Donnees.controlAction(this, Donnees.MODIFIER_NOTES, new update(this, 2, n));
        }
        else {
            JOptionPane.showMessageDialog(null, "Aucun etudiant n'a ete selectionne", "Selection non valide", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

