    private void initTableau(){
        DefaultTableModel model = (DefaultTableModel) listeNotes.getModel();
        model.setRowCount(0);
        for(int i = 0; i < Donnees.notes.size(); i++){
            Note n = Donnees.notes.get(i);
            model.addRow(new Object[]{n.getCode_matiere(), n.getNote()});
        }
    }

