    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int l = listeNotes.getSelectedRow();
        if(listeNotes.getSelectedRowCount() == 1) {
            if(Donnees.profileUtilisateurConnecte.getPrivileges()[Donnees.SUPPRIMER_NOTES] == '1'){
                Note m = new Note(
                    listeNotes.getModel().getValueAt(l, 0).toString(),
                    matricule_etudiant.getText(),
                    Integer.parseInt(listeNotes.getModel().getValueAt(l, 1).toString())
                );
                m.delete();
            }
            else {
                Donnees.actionInterdite(this);
            }
            
        }
        else {
            JOptionPane.showMessageDialog(null, "Aucune note n'a ete selectionnee", "Selection non valide", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

