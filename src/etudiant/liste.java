    private void initTableau(){
        DefaultTableModel model = (DefaultTableModel) listeEtudiants.getModel();
        model.setRowCount(0);
        for(int i = 0; i < Donnees.etudiants.size(); i++){
            Etudiant e = Donnees.etudiants.get(i);
            model.addRow(new Object[]{e.getMatricule(), e.getNom(), e.getPrenom(), e.getId_classe(), e.getSexe(), e.getDate_naiss(), e.getQuartier()});
        }
    }
