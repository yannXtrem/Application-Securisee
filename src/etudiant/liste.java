    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if(listeEtudiants.getSelectedRowCount() == 1) {
            if(Donnees.profileUtilisateurConnecte.getPrivileges()[Donnees.SUPPRIMER_ETUDIANTS] == '1'){
                int l = listeEtudiants.getSelectedRow();
                Etudiant e = new Etudiant(
                        listeEtudiants.getModel().getValueAt(l, 0).toString(),
                        listeEtudiants.getModel().getValueAt(l, 1).toString(),
                        listeEtudiants.getModel().getValueAt(l, 2).toString(),
                        Integer.parseInt(listeEtudiants.getModel().getValueAt(l, 3).toString()),
                        listeEtudiants.getModel().getValueAt(l, 4).toString().charAt(0),
                        Date.valueOf(listeEtudiants.getModel().getValueAt(l, 5).toString()),
                        listeEtudiants.getModel().getValueAt(l, 6).toString()
                );
                e.delete();
            }
            else Donnees.actionInterdite(this);
        }
        else {
            JOptionPane.showMessageDialog(this, "Aucun etudiant n'a ete selectionne", "Selection non valide", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

