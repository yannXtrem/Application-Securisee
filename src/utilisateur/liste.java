    private void initTableau(){
        DefaultTableModel model = (DefaultTableModel) listeUtilisateurs.getModel();
        model.setRowCount(0);
        for(int i = 0; i < Donnees.utilisateurs.size(); i++){
            Utilisateur u = Donnees.utilisateurs.get(i);
            model.addRow(new Object[]{u.getNom(), u.getMdp(),u.getProfile()});
        }
    }
