    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if(listeUtilisateurs.getSelectedRowCount() == 1) {
            int l = listeUtilisateurs.getSelectedRow();
            if(Donnees.profileUtilisateurConnecte.getPrivileges()[Donnees.SUPPRIMER_UTILISATEURS] == '1'){
                Utilisateur u = new Utilisateur(
                    listeUtilisateurs.getModel().getValueAt(l, 0).toString(),
                    listeUtilisateurs.getModel().getValueAt(l, 1).toString(),
                    listeUtilisateurs.getModel().getValueAt(l, 2).toString()
                );
                u.delete();
            }
            else Donnees.actionInterdite(this);
        }
        else {
            JOptionPane.showMessageDialog(this, "Aucun utilisateur n'a ete selectionne", "Selection non valide", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        
        if(listeUtilisateurs.getSelectedRowCount() == 1) {
            Donnees.controlAction(this, Donnees.MODIFIER_UTILISATEURS, new update(this, Donnees.utilisateurs.get(listeUtilisateurs.getSelectedRow()), 1));
        }
        else {
            JOptionPane.showMessageDialog(this, "Aucun utilisateur n'a ete selectionne", "Selection non valide", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

