    private void initTableau(){
        DefaultTableModel model = (DefaultTableModel) listeProfiles.getModel();
        model.setRowCount(0);
        for(int i = 0; i < Donnees.profiles.size(); i++){
            Profile p = Donnees.profiles.get(i);
            model.addRow(new Object[]{p.getNom(), p.getPrivileges()});
        }
    }
