    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if(listeProfiles.getSelectedRowCount() == 1) {
            int l = listeProfiles.getSelectedRow();

            Profile u = new Profile(
                listeProfiles.getModel().getValueAt(l, 0).toString(),
                listeProfiles.getModel().getValueAt(l, 1).toString()
            );
            u.delete();
            initTableau();
        }
        else {
            JOptionPane.showMessageDialog(this, "Aucun profile n'a ete selectionne", "Selection non valide", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void listeProfilesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_listeProfilesMouseClicked
        // TODO add your handling code here:
        if(listeProfiles.getSelectedRowCount() == 1) {
            Profile p = Donnees.profiles.get(listeProfiles.getSelectedRow());
            initCheckBoxes(p.getPrivileges());
        }
    }//GEN-LAST:event_listeProfilesMouseClicked

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        if(listeProfiles.getSelectedRowCount() == 1) {
            Profile p = Donnees.profiles.get(listeProfiles.getSelectedRow());
            createP c = new createP(this, 1, p);
            c.setVisible(true);
            this.setVisible(false);
        }
        else {
            JOptionPane.showMessageDialog(this, "Aucun profile n'a ete selectionne", "Selection non valide", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

